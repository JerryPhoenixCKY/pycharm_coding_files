"""
.	点号	     匹配任意单个字符（除换行符 \n）	a.c → 匹配 "abc", "a2c", "a c"
^	脱字符	 匹配字符串开头	^Hello → 匹配 "Hello world"，不匹配 "Say Hello"
$	美元符	 匹配字符串结尾	world$ → 匹配 "Hello world"，不匹配 "world!"
*	星号	     匹配前面的字符 0 次或多次	ab*c → 匹配 "ac", "abc", "abbbc"
+   加号      匹配前面的字符 1 次或多次	ab+c → 匹配 "abc", "abbbc"，不匹配 "ac"
?   问号      匹配前面的字符 0 次或 1 次（可选）	colou?r → 匹配 "color" 和 "colour"
{n}	花括号	 匹配前面的字符恰好 n 次	a{3} → 匹配 "aaa"
{n,} 花括号	 匹配至少 n 次	a{2,} → 匹配 "aa", "aaa", ...
{n,m} 花括号	 匹配n 到 m 次	a{2,4} → 匹配 "aa", "aaa", "aaaa"
[]	方括号	字符集合，匹配其中任意一个字符	[aeiou] → 匹配任意元音字母
()	圆括号	分组，也可用于捕获子表达式	(ab)+ → 匹配 "ab", "abab"
\	反斜杠	转义字符，让元字符变回普通字符	\. → 匹配字面的点号 .
⚠️ 注意：{}, [], () 都是成对出现的，单独一个不算元字符。

🧩 三、常见预定义字符集（也含元字符）
这些是“快捷写法”，本质也是元字符组合：
表达式	含义	    等价于
\d	匹配任意数字	[0-9]
\D	匹配任意非数字	[^0-9]
\w	匹配字母、数字、下划线	[a-zA-Z0-9_]
\W	匹配非单词字符	[^a-zA-Z0-9_]
\s	匹配空白字符（空格、制表符、换行等）	[ \t\n\r\f\v]
\S	匹配非空白字符	[^ \t\n\r\f\v]
💡 \d, \w, \s 中的 \ 也是元字符（用于引入特殊序列）。


"""

"""
限定符	含义	非贪婪形式
*	0 次或多次	*?
+	1 次或多次	+?
?	0 次或 1 次	??
{n}	恰好 n 次	{n}?（效果同 {n}）
{n,}	至少 n 次	{n,}?
{n,m}	n 到 m 次	{n,m}?
"""


"""
正则表达式（Regular Expression）由多种类型的字符和结构组成，除了限定符（量词）元字符（如 *、+、?、{n}、{n,}、{n,m}）之外，还包括以下几类重要的字符和元素：

1. 普通字符（字面量字符）
这些字符没有特殊含义，匹配自身。例如：

a 匹配字母 a
1 匹配数字 1
@ 匹配符号 @
2. 基本元字符（非限定符类）
这些元字符具有特殊含义，用于表示通配、分组、锚定等：

元字符	含义
.	匹配任意单个字符（除换行符，除非使用 s 标志）
^	匹配字符串的开始（行首锚点）
$	匹配字符串的结束（行尾锚点）
\	转义字符，用于取消元字符的特殊含义，或引入特殊序列（如 \d）
`	`
|   选择字符匹配|左右的任意字符
( )	分组，用于捕获或应用量词到整个子表达式
[ ]	字符类，匹配括号内的任意一个字符（如 [abc]）
-	在字符类中表示范围（如 [a-z]）
^（在 [ ] 内）	表示否定（如 [^0-9] 表示非数字）

4. 断言（零宽匹配）
这些不消耗字符，只匹配位置：

断言	含义
^, $	行首/行尾（也可视为断言）
\b, \B	单词边界/非边界
(?=...)	正向先行断言（后面必须是...）
(?!...)	负向先行断言（后面不能是...）
(?<=...)	正向后行断言（前面必须是...）
(?<!...)	负向后行断言（前面不能是...）
注意：后行断言在部分正则引擎（如 JavaScript）中支持有限。

5. 特殊构造
(?:...)：非捕获组（分组但不保存匹配内容）
(?>...)：原子组（防止回溯，用于优化）
(?i), (?m), (?s) 等：内联标志（如忽略大小写、多行模式等）
"""



