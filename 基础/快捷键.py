

# - **查看所有快捷键**：`Ctrl + Shift + A`（输入功能名，如 “rename”，PyCharm 会告诉你对应快捷键）

# 字体大小shift加alt加，或。
# - **自定义快捷键**：`File → Settings → Keymap`（可切换为 Eclipse、VS Code 等风格）
# - **快速修复建议**：`Alt + Enter`（比如自动导入、创建方法、修复拼写等）
## 🔍 一、搜索与导航
#
# | 功能 | 快捷键 |
# |------|--------|
# | **全局搜索（任何文件/类/符号）** | `Double Shift`（连按两次 Shift） |
# | **查找（当前文件内）** | `Ctrl + F` |
# | **替换（当前文件内）** | `Ctrl + R` |
# | **全局查找（所有文件中）** | `Ctrl + Shift + F` |
# | **全局替换** | `Ctrl + Shift + R` |
# | **打开文件** | `Ctrl + Shift + N` |
# | **打开类（Class）** | `Ctrl + N` |
# | **打开方法/变量（Symbol）** | `Ctrl + Shift + Alt + N` |
# | **跳转到行号** | `Ctrl + G` |
# | **最近打开的文件** | `Ctrl + E` |
# | **最近编辑的文件** | `Ctrl + Shift + E` |
#
# ---

## ✍️ 二、代码编辑
#
# | 功能 | 快捷键 |
# |------|--------|
# | **删除当前行** | `Ctrl + Y` |
# | **复制当前行到下一行** | `Ctrl + D` |
# | **移动当前行向上/向下** | `Alt + Shift + ↑ / ↓` |
# | **注释/取消注释（行注释）** | `Ctrl + /` |
# | **块注释（选中多行）** | `Ctrl + Shift + /` |
# | **自动缩进（格式化选中代码）** | `Ctrl + Alt + I` |
# | **代码自动补全** | `Ctrl + Space` |
# | **智能补全（更精准）** | `Ctrl + Shift + Space` |
# | **生成代码（如 `__init__`, `getter/setter`）** | `Alt + Insert` |
# | **包围代码（如 try-except, if, for）** | `Ctrl + Alt + T` |
#
# ---
#
# ## 🧹 三、代码格式化与优化
#
# | 功能 | 快捷键 |
# |------|--------|
# | **格式化代码（按 PEP8 等规则）** | `Ctrl + Alt + L` |
# | **优化导入（自动删除未用 import）** | `Ctrl + Alt + O` |
# | **重命名（变量/函数/类，安全重构）** | `Shift + F6` |
# | **查找所有引用** | `Alt + F7` |
# | **跳转到定义** | `Ctrl + B` 或 `Ctrl + 鼠标左键` |
# | **查看文档（Docstring）** | `Ctrl + Q` |
#
# ---
#
# ## 🐞 四、调试与运行
#
# | 功能 | 快捷键 |
# |------|--------|
# | **运行当前脚本** | `Ctrl + Shift + F10` |
# | **调试当前脚本** | `Shift + F9` |
# | **继续执行（调试时）** | `F9` |
# | **单步跳过（Step Over）** | `F8` |
# | **单步进入（Step Into）** | `F7` |
# | **跳出当前函数（Step Out）** | `Shift + F8` |
# | **运行到光标处（Run to Cursor）** | `Alt + F9` |
# | **查看/添加断点** | `Ctrl + F8`（在行号处点击也可） |
#
# ---
#
# ## 🗂️ 五、项目与窗口管理
#
# | 功能 | 快捷键 |
# |------|--------|
# | **折叠/展开代码块** | `Ctrl + -` / `Ctrl + +` |
# | **折叠全部方法** | `Ctrl + Shift + -` |
# | **展开全部** | `Ctrl + Shift + +` |
# | **切换侧边栏（项目结构）** | `Alt + 1` |
# | **切换终端** | `Alt + F12` |
# | **切换到编辑器** | `Esc` |
# | **关闭当前标签页** | `Ctrl + F4` |
# | **切换标签页** | `Alt + ← / →` |

